package gun51;

public class IntNum {
    public static void main(String[] args) {

        int a = 1_00_00;    // 10000
        System.out.println(a);
        double d = 1_0_00.00_5;  // 1000.005
        System.out.println(d);

        long l1 = 121_212_121_212_1L;
        short s1 = 1_123;   // 1123
        float f1 = 1.2F;
    }
}
